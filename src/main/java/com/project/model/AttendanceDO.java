package com.project.model;

public class AttendanceDO {

	private int student_id;
	private String a_date;
	private String a_cintime;
	private String a_couttime;
	private String a_souttime;
	private String a_rettime;
	private int a_status;
	
	public AttendanceDO() {	
	}

	public int getStudent_id() {
		return student_id;
	}

	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}

	public String getA_date() {
		return a_date;
	}

	public void setA_date(String a_date) {
		this.a_date = a_date;
	}

	public String getA_cintime() {
		return a_cintime;
	}

	public void setA_cintime(String a_cintime) {
		this.a_cintime = a_cintime;
	}

	public String getA_couttime() {
		return a_couttime;
	}

	public void setA_couttime(String a_couttime) {
		this.a_couttime = a_couttime;
	}

	public String getA_souttime() {
		return a_souttime;
	}

	public void setA_souttime(String a_souttime) {
		this.a_souttime = a_souttime;
	}

	public String getA_rettime() {
		return a_rettime;
	}

	public void setA_rettime(String a_rettime) {
		this.a_rettime = a_rettime;
	}

	public int getA_status() {
		return a_status;
	}

	public void setA_status(int a_status) {
		this.a_status = a_status;
	}
	
	
	
}
