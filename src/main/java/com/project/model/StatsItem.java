package com.project.model;

public class StatsItem {
	private int presentCnt;
	private int tardyCnt;
	private int leaveCnt;
	private int absentCnt;
	private int totalCnt;
	private int myCnt;
	private int avgCnt;

	public int getPresentCnt() {
		return presentCnt;
	}

	public void setPresentCnt(int presentCnt) {
		this.presentCnt = presentCnt;
	}

	public int getTardyCnt() {
		return tardyCnt;
	}

	public void setTardyCnt(int tardyCnt) {
		this.tardyCnt = tardyCnt;
	}

	public int getLeaveCnt() {
		return leaveCnt;
	}

	public void setLeaveCnt(int leaveCnt) {
		this.leaveCnt = leaveCnt;
	}

	public int getAbsentCnt() {
		return absentCnt;
	}

	public void setAbsentCnt(int absentCnt) {
		this.absentCnt = absentCnt;
	}

	public int getTotalCnt() {
		return totalCnt;
	}

	public void setTotalCnt(int totalCnt) {
		this.totalCnt = totalCnt;
	}

	public int getMyCnt() {
		return myCnt;
	}

	public void setMyCnt(int myCnt) {
		this.myCnt = myCnt;
	}

	public int getAvgCnt() {
		return avgCnt;
	}

	public void setAvgCnt(int avgCnt) {
		this.avgCnt = avgCnt;
	}
}
