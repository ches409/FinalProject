package com.project.model;

public class CourseDay {

	private int d_mon;
	private int d_tue;
	private int d_wed;
	private int d_thu;
	private int d_fri;
	
	public CourseDay() {
	}

	public int getD_mon() {
		return d_mon;
	}

	public void setD_mon(int d_mon) {
		this.d_mon = d_mon;
	}

	public int getD_tue() {
		return d_tue;
	}

	public void setD_tue(int d_tue) {
		this.d_tue = d_tue;
	}

	public int getD_wed() {
		return d_wed;
	}

	public void setD_wed(int d_wed) {
		this.d_wed = d_wed;
	}

	public int getD_thu() {
		return d_thu;
	}

	public void setD_thu(int d_thu) {
		this.d_thu = d_thu;
	}

	public int getD_fri() {
		return d_fri;
	}

	public void setD_fri(int d_fri) {
		this.d_fri = d_fri;
	}
	
	
	
}
